package penjualanroti;

/**
 *
 * @author zafri
 */
public class PenjualanRoti {

private String Roti;
private String jenis;
private int harga;


public String getRoti ()
{
return Roti;
}
 
public String getJenis ()
{
return jenis;
}
 
public int getHarga()
{
return harga;
}


public void setRoti (String RotiBaru)
{
Roti = RotiBaru;
}
public void setJenis (String jenisBaru)
{
this.jenis = jenisBaru;
}
 
public void setHarga (int hargaBaru)
{
harga = hargaBaru;
 }}


    
